/**
 * ==========================================================
 * PostgreSQL (Postgres) – সহজ ব্যাখ্যা
 * ==========================================================
 *
 * 1️⃣ PostgreSQL কী?
 * --------------------------------
 * PostgreSQL হলো একটি ফ্রি ও ওপেন-সোর্স
 * রিলেশনাল ডাটাবেস ম্যানেজমেন্ট সিস্টেম (RDBMS)।
 * এটি ডাটাকে টেবিল আকারে সংরক্ষণ, পরিচালনা
 * এবং খোঁজার জন্য ব্যবহৃত হয়।
 *
 * সহজভাবে:
 * PostgreSQL = সফটওয়্যার যা টেবিলের মধ্যে ডাটা রাখে
 * এবং SQL ব্যবহার করে তা খুঁজে বের করা যায়।
 *
 * ---------------------------------------------------------
 * 2️⃣ Postgres কী?
 * --------------------------------
 * Postgres হলো PostgreSQL-এর সংক্ষিপ্ত নাম।
 * অর্থাৎ, Postgres = PostgreSQL
 *
 * এটি একই ফ্রি, শক্তিশালী এবং নিরাপদ ডাটাবেস সফটওয়্যার।
 *
 * ---------------------------------------------------------
 * 3️⃣ PostgreSQL এর মূল বৈশিষ্ট্য
 * --------------------------------
 * ✅ Open-source এবং সম্পূর্ণ ফ্রি
 * ✅ SQL (Structured Query Language) ব্যবহার করে
 * ✅ JSON/NoSQL সাপোর্ট করে
 * ✅ ACID compliant → নিরাপদ ট্রানজ্যাকশন
 * ✅ বড় ও জটিল ডাটাও সহজে হ্যান্ডেল করতে পারে
 * ✅ Windows, Linux, macOS-এ কাজ করে
 *
 * ---------------------------------------------------------
 * 4️⃣ উদাহরণ
 * --------------------------------
 * ধরুন, একটি ওয়েবসাইটে user ডাটাবেস আছে:
 *
 * User Table:
 * ------------------------
 * id | name  | email  | password
 *
 * Postgres এখানে ব্যবহৃত হয়:
 * - ডাটা সংরক্ষণ করতে
 * - প্রয়োজন অনুযায়ী ডাটা খুঁজে বের করতে
 *
 * উদাহরণ SQL কমান্ড:
 * SELECT * FROM User WHERE id = 101;
 *
 * ---------------------------------------------------------
 * সংক্ষেপে:
 * PostgreSQL বা Postgres = শক্তিশালী, নিরাপদ, ওপেন-সোর্স
 * ডাটাবেস ম্যানেজমেন্ট সিস্টেম।
 *
 * ==========================================================
 */
