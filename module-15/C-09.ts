/**
 * SDLC: Software Development Life Cycle
 * বাংলায়: সফটওয়্যার উন্নয়ন জীবনচক্র
 *
 * SDLC হলো একটি প্রক্রিয়া বা ধাপগুলোর সিরিজ, যার মাধ্যমে
 * সফটওয়্যার বা অ্যাপ্লিকেশন পরিকল্পনা, তৈরি, পরীক্ষা ও রক্ষণাবেক্ষণ করা হয়।
 *
 * SDLC-এর মূল ধাপসমূহ:
 *
 * 1. Planning (পরিকল্পনা)
 *    - প্রজেক্টের লক্ষ্য, সময়সীমা, বাজেট নির্ধারণ।
 *    - কী সমস্যা সমাধান করা হবে বা কী চাহিদা পূরণ হবে তা বোঝা।
 *
 * 2. Requirement Analysis (চাহিদা বিশ্লেষণ)
 *    - ব্যবহারকারীর চাহিদা (user requirements) সংগ্রহ করা।
 *    - ঠিক কি কি ফিচার লাগবে, কী ধরনের ডেটা দরকার তা বোঝা।
 *
 * 3. Design (ডিজাইন)
 *    - সফটওয়্যার কিভাবে কাজ করবে তা পরিকল্পনা করা।
 *    - Architectural design, database design, UI/UX design তৈরি করা।
 *
 * 4. Implementation / Coding (বাস্তবায়ন / কোডিং)
 *    - ডিজাইন অনুযায়ী কোড লেখা।
 *    - সফটওয়্যার তৈরি করা।
 *
 * 5. Testing (পরীক্ষা)
 *    - সফটওয়্যার ঠিকমতো কাজ করছে কি না তা যাচাই করা।
 *    - Bugs বা সমস্যা খুঁজে বের করে ঠিক করা।
 *
 * 6. Deployment (প্রকাশ / ব্যবহার শুরু)
 *    - সফটওয়্যার ব্যবহারকারীর কাছে দেওয়া বা সার্ভারে আপলোড করা।
 *
 * 7. Maintenance (রক্ষণাবেক্ষণ)
 *    - সফটওয়্যার চলাকালীন সময় নতুন চাহিদা, আপডেট বা সমস্যার সমাধান করা।
 *
 * সংক্ষিপ্ত মানে:
 * - SDLC হলো সফটওয়্যার তৈরির ধাপগুলোর নিয়মিত প্রক্রিয়া।
 * - এটি নিশ্চিত করে সফটওয়্যার সঠিকভাবে, সময়মতো ও কার্যকরভাবে তৈরি হয়।
 */
